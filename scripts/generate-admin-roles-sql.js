const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const outputPath = path.join(root, "db", "generated-admin-roles.sql");
const writeFile = process.argv.includes("--write");

function splitIds(value) {
  return String(value || "")
    .split(",")
    .map(item => item.trim())
    .filter(Boolean);
}

function unique(values) {
  return [...new Set(values)];
}

function validateIds(label, ids, required = false) {
  if (required && !ids.length) {
    throw new Error(`${label} must include at least one discord:numeric-id value.`);
  }

  const invalid = ids.find(id => !/^discord:\d{16,22}$/.test(id) || /replace|your-|trusted-|smoke-|\.\.\./i.test(id));
  if (invalid) {
    throw new Error(`${label} contains an invalid account id: ${invalid}`);
  }
}

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function valuesSql(ids) {
  return ids.map(id => `    (${sqlString(id)})`).join(",\n");
}

function render(adminIds, moderatorIds) {
  const moderatorSection = moderatorIds.length ? `
with moderator_accounts(provider_user_id) as (
  values
${valuesSql(moderatorIds)}
)
insert into profiles (provider, provider_user_id, display_name, role, status)
select 'local', provider_user_id, provider_user_id, 'moderator', 'active'
from moderator_accounts
on conflict (provider, provider_user_id) do update set
  role = case when profiles.role = 'admin' then 'admin' else 'moderator' end,
  status = 'active',
  updated_at = now();
` : `
-- No MODERATOR_ACCOUNT_IDS were provided.
`;

  return `-- Generated by scripts/generate-admin-roles-sql.js.
-- Source: ADMIN_ACCOUNT_IDS and MODERATOR_ACCOUNT_IDS.
-- Review before applying to production.

begin;

with admin_accounts(provider_user_id) as (
  values
${valuesSql(adminIds)}
)
insert into profiles (provider, provider_user_id, display_name, role, status)
select 'local', provider_user_id, provider_user_id, 'admin', 'active'
from admin_accounts
on conflict (provider, provider_user_id) do update set
  role = 'admin',
  status = 'active',
  updated_at = now();
${moderatorSection}
commit;
`;
}

function main() {
  const adminIds = unique(splitIds(process.env.ADMIN_ACCOUNT_IDS));
  const moderatorIds = unique(splitIds(process.env.MODERATOR_ACCOUNT_IDS)).filter(id => !adminIds.includes(id));

  validateIds("ADMIN_ACCOUNT_IDS", adminIds, true);
  validateIds("MODERATOR_ACCOUNT_IDS", moderatorIds, false);

  const sql = render(adminIds, moderatorIds);
  if (writeFile) {
    fs.writeFileSync(outputPath, sql);
    console.log(`Generated ${path.relative(root, outputPath)}`);
    return;
  }

  process.stdout.write(sql);
}

main();
