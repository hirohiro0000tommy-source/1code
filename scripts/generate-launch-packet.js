const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const outDir = path.join(root, "dist");
const outPath = path.join(outDir, "launch-packet.md");

function env(name) {
  return String(process.env[name] || "").trim();
}

function state(name) {
  return env(name) ? "set" : "missing";
}

function publicBaseUrlState() {
  const value = env("PUBLIC_BASE_URL");
  if (!value) return "missing";
  try {
    const parsed = new URL(value);
    const originOnly = (!parsed.pathname || parsed.pathname === "/") && !parsed.search && !parsed.hash;
    return parsed.protocol === "https:" && originOnly && !/localhost|127\.0\.0\.1/i.test(parsed.hostname) ? "ok" : "check";
  } catch (error) {
    return "invalid";
  }
}

function secretState(name) {
  return env(name) ? "set in environment" : "missing";
}

function line(text = "") {
  return text;
}

const generatedAt = new Date().toISOString();
const publicBaseUrl = env("PUBLIC_BASE_URL") || "https://YOUR-PUBLIC-URL";
const discordRedirect = `${publicBaseUrl.replace(/\/$/, "")}/auth/discord/callback`;
const envRows = [
  ["NODE_ENV", state("NODE_ENV"), "production", false],
  ["STORAGE_DRIVER", state("STORAGE_DRIVER"), "postgres", false],
  ["DATABASE_URL", secretState("DATABASE_URL"), "real production Postgres URL", true],
  ["DATABASE_SSL", state("DATABASE_SSL"), "true", false],
  ["PUBLIC_BASE_URL", publicBaseUrlState(), "final https origin only", false],
  ["PUBLIC_SECURITY_CONTACT", state("PUBLIC_SECURITY_CONTACT"), "real mailto: or https contact", false],
  ["ADMIN_PIN", secretState("ADMIN_PIN"), "generated by npm run secrets", true],
  ["SESSION_SECRET", secretState("SESSION_SECRET"), "generated by npm run secrets", true],
  ["ADMIN_ACCOUNT_IDS", state("ADMIN_ACCOUNT_IDS"), "discord:numeric-id", false],
  ["MODERATOR_ACCOUNT_IDS", state("MODERATOR_ACCOUNT_IDS"), "blank or discord:numeric-id", false],
  ["DISCORD_CLIENT_ID", state("DISCORD_CLIENT_ID"), "Discord application client ID", false],
  ["DISCORD_CLIENT_SECRET", secretState("DISCORD_CLIENT_SECRET"), "Discord application secret", true],
  ["BETA_ACCESS_CODE", env("BETA_ACCESS_CODE") ? "set" : "blank", "blank for public launch", true],
  ["PUBLIC_WRITE_PAUSED", env("PUBLIC_WRITE_PAUSED") || "missing", "false for launch / true for emergency", false],
  ["ENABLE_SEED_DATA", env("ENABLE_SEED_DATA") || "missing", "false", false],
  ["RELEASE_VERSION", state("RELEASE_VERSION"), "release label", false],
  ["COMMIT_SHA", state("COMMIT_SHA"), "deployed commit", false]
];

